'use strict'
//
// Keep electron-updater's latest-*.yml consistent with the artifacts that
// actually ship. notarize-dist.js repackages the zip (from the stapled app)
// and re-signs/staples the dmg AFTER electron-builder generated the yml, so
// the yml's sha512/size describe pre-notarization bytes and the updater
// fails with a checksum mismatch right after download. These helpers
// recompute the hashes from the final files and rewrite the yml in place.
const { createHash } = require('crypto')
const fs = require('fs')
const path = require('path')

/**
 * Rewrite sha512/size values in latest-*.yml text. `statsByName` maps an
 * artifact file name (the `url:`/`path:` value) to its final { sha512, size }.
 * Files without stats keep their existing values. Line-based on purpose: the
 * yml is machine-generated by electron-builder, and this preserves every
 * field it does not understand.
 */
function updateLatestYmlText(ymlText, statsByName) {
  let currentFile = null
  return ymlText
    .split('\n')
    .map((line) => {
      const fileRef = line.match(/^(?:\s*-\s+url|path):\s*(.+?)\s*$/)
      if (fileRef) {
        currentFile = fileRef[1]
        return line
      }
      const field = line.match(/^(\s*)(sha512|size):\s*.+$/)
      if (field && currentFile && statsByName[currentFile]) {
        return `${field[1]}${field[2]}: ${statsByName[currentFile][field[2]]}`
      }
      return line
    })
    .join('\n')
}

/**
 * Recompute sha512/size for every artifact listed in `dir/<ymlName>` that
 * exists on disk and rewrite the yml. Returns the file names that were
 * re-hashed (empty if the yml itself is missing).
 */
function regenerateLatestYml(dir, ymlName) {
  const ymlPath = path.join(dir, ymlName)
  if (!fs.existsSync(ymlPath)) return []
  const ymlText = fs.readFileSync(ymlPath, 'utf8')

  const listed = new Set()
  for (const match of ymlText.matchAll(/^(?:\s*-\s+url|path):\s*(.+?)\s*$/gm)) {
    listed.add(match[1])
  }

  const statsByName = {}
  for (const name of listed) {
    const filePath = path.join(dir, name)
    if (!fs.existsSync(filePath)) continue
    statsByName[name] = {
      sha512: createHash('sha512').update(fs.readFileSync(filePath)).digest('base64'),
      size: fs.statSync(filePath).size,
    }
  }

  fs.writeFileSync(ymlPath, updateLatestYmlText(ymlText, statsByName))
  return Object.keys(statsByName)
}

module.exports = { updateLatestYmlText, regenerateLatestYml }
